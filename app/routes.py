@router.post("/auth/register", status_code=status.HTTP_201_CREATED)
async def register_user(
    body: UserRegisterRequest,
    db: AsyncSession = Depends(get_db_session)
):
    """Registers a new user account with a secure hashed password."""
    # Check if username is already taken
    statement = select(User).where(User.username == body.username)
    result = await db.execute(statement)
    existing_user = result.scalar_one_or_none()
    
    if existing_user:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Username is already taken."
        )
        
    hashed_pwd = get_password_hash(body.password)
    user_record = User(username=body.username, hashed_password=hashed_pwd)
    
    db.add(user_record)
    await db.commit()
    
    return {"detail": "User registered successfully."}


@router.post("/auth/login", response_model=TokenResponse)
async def login_user(
    body: UserLoginRequest,
    db: AsyncSession = Depends(get_db_session)
):
    """Validates login credentials and returns a JWT access token."""
    statement = select(User).where(User.username == body.username)
    result = await db.execute(statement)
    user = result.scalar_one_or_none()
    
    if not user or not verify_password(body.password, user.hashed_password):
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="Invalid username or password."
        )
        
    access_token = create_access_token(data={"sub": user.username})
    return TokenResponse(access_token=access_token, token_type="bearer")


